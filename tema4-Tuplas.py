'''
Las Tuplas son Inmutables 
'''

tupla=("uno","dos","tres")
print(tupla)

tuplaVariableDatos=(12,True,23.5,"El gato",12+2j)
print(tuplaVariableDatos)

tumplasConTuplas=(1,2,3,4(1,2,3))
print(tumplasConTuplas)
print(tuplaVariableDatos[3])
print(tuplaVariableDatos[-2])
print(tuplaVariableDatos[0:3])

a,b,c=tupla
print(a)
print(b)
print(c)

tumplaNueva=tuplaVariableDatos
print(tumplaNueva)